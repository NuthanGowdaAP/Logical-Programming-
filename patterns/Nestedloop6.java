package com.java.patterns;
public class Nestedloop6 {
    public static void main(String[] args)
    {
     int n='C';
     for(int i='A';i<=n;i++) 
     {
       for(int j='A';j<=n;j++)
       {
         System.out.print(j +" ");
       } 
       System.out.println();
     }  
   }

}
